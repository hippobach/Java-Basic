public class Bai7 {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a = " +a);
        System.out.println("b = " +b);

//        + operator
        System.out.println("a+b=" + (a+b));
//        - operator
        System.out.println("a-b=" + (a-b));
//        * operator
        System.out.println("a*b=" + (a*b));
//        / operator  => casting type
        System.out.println("a/b=" + ((double)a/b));
//        mod operator
        System.out.println("a%b=" + (a%b));
    }
}
